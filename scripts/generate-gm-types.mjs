export function buildGmTypesDeclaration() {
  return DECLARATION;
}

export function generateGmTypes(options = {}) {
  const rootDir = resolve(options.rootDir || DEFAULT_ROOT);
  const outFile = join(rootDir, RELATIVE_OUT_FILE);
  const check = Boolean(options.check);

  mkdirSync(dirname(outFile), { recursive: true });

  if (check) {
    if (!existsSync(outFile)) {
      throw new Error(`[gm-types] missing ${outFile}`);
    }
    const current = readFileSync(outFile, 'utf8');
    if (current !== DECLARATION) {
      throw new Error('[gm-types] lib/scriptvault.d.ts is out of date. Run npm run gm-types:generate.');
    }
    console.log('[gm-types] ok: lib/scriptvault.d.ts');
    return outFile;
  }

  writeFileSync(outFile, DECLARATION, 'utf8');
  console.log('[gm-types] wrote lib/scriptvault.d.ts');
  return outFile;
}

function parseCliOptions(argv) {
  return {
    check: argv.includes('--check'),
    rootDir: DEFAULT_ROOT,
  };
}

function main() {
  try {
    generateGmTypes(parseCliOptions(process.argv.slice(2)));
  } catch (error) {
    console.error(error?.message || error);
    process.exit(1);
  }
}

if (process.argv[1] && resolve(process.argv[1]) === SCRIPT_FILE) {
  main();
}
