export const WARNING_BUDGET = 53;

export function summarizeFirefoxLintReport(report) {
  const warnings = Array.isArray(report?.warnings) ? report.warnings : [];
  const byCode = {};
  const byFile = {};
  for (const warning of warnings) {
    const code = String(warning?.code || 'UNKNOWN');
    const file = String(warning?.file || '<unknown>');
    byCode[code] = (byCode[code] || 0) + 1;
    byFile[file] = (byFile[file] || 0) + 1;
  }
  return { total: warnings.length, byCode, byFile };
}

export function evaluateFirefoxLintWarnings(
  report,
  {
    budget = WARNING_BUDGET,
    reviewedCodes = REVIEWED_CODES,
    reviewedFiles = REVIEWED_WARNING_FILES,
  } = {},
) {
  const summary = summarizeFirefoxLintReport(report);
  const failures = [];
  for (const code of Object.keys(summary.byCode)) {
    if (!reviewedCodes[code]) {
      failures.push(`Unreviewed warning code: ${code} (${summary.byCode[code]} occurrences). Add rationale to REVIEWED_CODES in this script.`);
    }
  }
  for (const file of Object.keys(summary.byFile)) {
    if (!reviewedFiles.has(file)) {
      failures.push(`Unreviewed warning file: ${file} (${summary.byFile[file]} occurrences). Review the warning and baseline the file explicitly.`);
    }
  }
  if (summary.total > budget) {
    failures.push(`Warning count ${summary.total} exceeds budget of ${budget}. Reduce warnings or raise the budget with justification.`);
  }
  return { ...summary, failures };
}

function main() {
  const reportMode = process.argv.includes('--report');

  if (!existsSync(LINT_REPORT)) {
    if (reportMode) {
      console.log('No lint report found at', LINT_REPORT);
      console.log('Run: npm run firefox:lint');
      return;
    }
    console.error('Firefox lint report not found:', LINT_REPORT);
    console.error('Run: npm run firefox:lint');
    process.exit(1);
  }

  const report = JSON.parse(readFileSync(LINT_REPORT, 'utf8'));
  const { total, byCode, byFile, failures } = evaluateFirefoxLintWarnings(report);

  if (reportMode) {
    console.log(`Firefox lint warning inventory: ${total} warnings\n`);
    console.log('By code:');
    for (const [code, count] of Object.entries(byCode).sort((a, b) => b[1] - a[1])) {
      const reviewed = REVIEWED_CODES[code] ? '[REVIEWED]' : '[UNREVIEWED]';
      console.log(`  ${count.toString().padStart(4)} ${code} ${reviewed}`);
    }
    console.log('\nBy file:');
    for (const [file, count] of Object.entries(byFile).sort((a, b) => b[1] - a[1])) {
      console.log(`  ${count.toString().padStart(4)} ${file}`);
    }
    if (total > 0) {
      console.log('\nReviewed rationale:');
      for (const [code, rationale] of Object.entries(REVIEWED_CODES)) {
        if (byCode[code]) {
          console.log(`  ${code}: ${rationale}`);
        }
      }
    }
    return;
  }

  if (failures.length > 0) {
    console.error(`Firefox lint warning budget: ${failures.length} failure(s)\n`);
    for (const failure of failures) console.error(`- ${failure}`);
    console.error(`\nTotal: ${total} warnings (budget: ${WARNING_BUDGET})`);
    process.exit(1);
  }

  console.log(`Firefox lint warning budget passed: ${total} warnings (budget: ${WARNING_BUDGET}), all codes reviewed.`);
}

if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main();
}
