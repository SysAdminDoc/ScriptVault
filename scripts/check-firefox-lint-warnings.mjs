#!/usr/bin/env node

/**
 * Firefox lint warning budget gate.
 *
 * Reads the checked-in web-ext lint report and verifies that:
 * 1. All warning codes are in the reviewed allowlist with rationale.
 * 2. Total warning count has not increased beyond the budget ceiling.
 * 3. No new files appear with warnings that aren't in the per-file inventory.
 *
 * Run: node scripts/check-firefox-lint-warnings.mjs [--report]
 *   --report: print the full warning inventory without failing
 */
import { readFileSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(SCRIPT_DIR, '..');
const LINT_REPORT = resolve(ROOT, 'firefox-artifacts/web-ext-lint.json');

export const REVIEWED_CODES = {
  UNSAFE_VAR_ASSIGNMENT: 'Dynamic HTML is centralized through safeSetHtml() / _safeSetHtml() and rendered with Range#createContextualFragment plus replaceChildren. Inputs are generated by ScriptVault renderers with explicit escapeHtml() on user/script data; raw innerHTML assignment was removed from dashboard, popup, install, devtools, and dashboard module surfaces.',
  UNSUPPORTED_API: 'Chrome-only API feature-detection probes (identity.getAuthToken, userScripts.execute, notifications.update, permissions.addHostAccessRequest). Code handles absence gracefully with try/catch or typeof checks. Required for cross-browser compatibility.',
  DANGEROUS_EVAL: 'Generated wrapper code uses Function constructor for GM API injection into USER_SCRIPT world. This is the core mechanism for userscript execution and cannot be replaced without abandoning the extension purpose. Equivalent to Tampermonkey/Violentmonkey architecture.',
  INLINE_SCRIPT: 'Packaged HTML pages keep minimal inline bootstrap scripts for extension-page startup and sandbox loading. Script execution stays local to packaged extension pages; pages/editor-sandbox.html is declared in manifest.json sandbox section with restricted CSP.',
  KEY_FIREFOX_ANDROID_UNSUPPORTED_BY_MIN_VERSION: 'Firefox Android minimum version warning. gecko_android.strict_min_version is intentionally lower to maximize compatibility; feature detection handles API gaps.',
};

